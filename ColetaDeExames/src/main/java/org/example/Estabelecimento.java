package org.example;

public class Estabelecimento {
    String rua;
    String numero;
    String estado;
    String cep;
    String telefone;

    public Estabelecimento(String rua, String numero, String estado, String cep, String telefone) {
        this.rua = rua;
        this.numero = numero;
        this.estado = estado;
        this.cep = cep;
        this.telefone = telefone;
    }
}
